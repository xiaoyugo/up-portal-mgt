package com.changhong.foodmanage.model;

public class Foodno {
	private String queryitem;
	private String selectfood;
	private String itemnom;
	private String itemname;
	private String itemsize;
	private String unitno;
	private String productarea;

	public String getQueryitem() {
		return queryitem;
	}
	public void setQueryitem(String queryitem) {
		this.queryitem = queryitem;
	}

	public String getSelectfood() {
		return selectfood;
	}
	public void setSelectfood(String selectfood) {
		this.selectfood = selectfood;
	}
	public String getItemnom() {
		return itemnom;
	}
	public void setItemnom(String itemnom) {
		this.itemnom = itemnom;
	}
	public String getItemname() {
		return itemname;
	}
	public void setItemname(String itemname) {
		this.itemname = itemname;
	}
	public String getItemsize() {
		return itemsize;
	}
	public void setItemsize(String itemsize) {
		this.itemsize = itemsize;
	}
	public String getUnitno() {
		return unitno;
	}
	public void setUnitno(String unitno) {
		this.unitno = unitno;
	}
	public String getProductarea() {
		return productarea;
	}
	public void setProductarea(String productarea) {
		this.productarea = productarea;
	}

}
